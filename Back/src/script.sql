CREATE TABLE ARTICLES (
  ID    INT PRIMARY KEY AUTO_INCREMENT,
  TITLE VARCHAR(20)
);

CREATE TABLE PARAGRAPHE (
  ID         INT PRIMARY KEY AUTO_INCREMENT,
  CONTENT    VARCHAR(10000),
  POSITION   INT,
  ARTICLE_ID INT,
  FOREIGN KEY (ARTICLE_ID) REFERENCES ARTICLES (ID)
);

