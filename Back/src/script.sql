DROP TABLE IF EXISTS PARAGRAPHE;
DROP TABLE IF EXISTS ARTICLES;

CREATE TABLE ARTICLES (
  ID    INT PRIMARY KEY AUTO_INCREMENT,
  TITLE VARCHAR(255)
);

CREATE TABLE PARAGRAPHE (
  ID         INT PRIMARY KEY AUTO_INCREMENT,
  CONTENT    TEXT,
  POSITION   INT,
  ARTICLE_ID INT,
  FOREIGN KEY (ARTICLE_ID) REFERENCES ARTICLES (ID)
);

